function setLanguage(lang) {
  if (!I18N[lang]) lang = "en";
  var dict = I18N[lang];
  document.querySelectorAll("[data-i18n]").forEach(function (el) {
    var key = el.getAttribute("data-i18n");
    if (dict[key] != null) el.innerHTML = dict[key];
  });
  document.documentElement.lang = lang;
  if (META[lang]) {
    document.title = META[lang].title;
    var m = document.querySelector('meta[name="description"]');
    if (m) m.setAttribute("content", META[lang].desc);
  }
  document.querySelectorAll(".lang-switch button").forEach(function (b) {
    b.classList.toggle("active", b.getAttribute("data-lang") === lang);
  });
  try { localStorage.setItem("chingu_lang", lang); } catch (e) {}
}

(function () {
  var saved;
  try { saved = localStorage.getItem("chingu_lang"); } catch (e) {}
  var lang = saved || ((navigator.language || "en").toLowerCase().indexOf("it") === 0 ? "it" : "en");
  setLanguage(lang);

  document.querySelectorAll(".lang-switch button").forEach(function (b) {
    b.addEventListener("click", function () { setLanguage(b.getAttribute("data-lang")); });
  });
})();
